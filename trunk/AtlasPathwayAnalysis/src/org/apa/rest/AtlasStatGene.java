package org.apa.rest;

public class AtlasStatGene {
	private String ef;
	private String efv;
	private AtlasStatValue stat;
	
	public String getEf() {
		return ef;
	}
	public void setEf(String ef) {
		this.ef = ef;
	}
	public String getEfv() {
		return efv;
	}
	public void setEfv(String efv) {
		this.efv = efv;
	}
	public AtlasStatValue getStat() {
		return stat;
	}
	public void setStat(AtlasStatValue stat) {
		this.stat = stat;
	}
}
